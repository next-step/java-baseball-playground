package training1;

public class CalculatorUtil {

    public static int add(int num1, int num2){
        return num1+num2;
    }
    public static int subtract(int num1, int num2){
        return num1-num2;
    }
    public static int multiple(int num1, int num2){
        return num1*num2;
    }
    public static int divide(int num1, int num2){
        return num1/num2;
    }

}
